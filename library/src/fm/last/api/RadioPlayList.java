public class RadioPlayList implements Serializable {

  private static final long serialVersionUID = -2942384510726687260L;
  private String title;
  private String creator;
  private String date;
  private String link;
  private String id;
  private boolean streamable;
  private RadioTrack[] tracks;

  public RadioPlayList(String title, String creator, String date, String link,
                       RadioTrack[] tracks, String id, boolean streamable) {
    this.title = title;
    this.creator = creator;
    this.date = date;
    this.link = link;
    this.tracks = tracks;
    this.id = id;
    this.streamable = streamable;
  }

  public String getTitle() {
    return title;
  }

  public String getCreator() {
    return creator;
  }

  public String getDate() {
    return date;
  }

  public String getLink() {
    return link;
  }

  public RadioTrack[] getTracks() {
    return tracks;
  }
  
  public String getId() {
	  return id;
  }
  
  public boolean isStreamable() {
	  return streamable;
  }
}
